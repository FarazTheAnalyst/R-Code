# Generate Sequence Starting from One
# define sequence
x <- seq(20)

# view sequence
x

# Generate Sequence with Specific Start and End Values
# define sequence
x <- seq(from=5, to=15)

# view sequence
x

# Generate Sequence with Custom Incrementing
# define sequence
x <- seq(from=0, to=20, by=4)

# view sequence
x

# Generate Sequence with Specific Length
# define sequence
x <- seq(from=0, to=20, length.out=4)

# view sequence
x

# Generate Sequence with Length Based on Some Data Object
# define vector y
y <- c(1, 4, 6, 9)

# define sequence x, make sure length matches the length of y
x <- seq(from = 0, to = 20, along.with = y)

# view sequence
x



















