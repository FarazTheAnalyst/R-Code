# create variable with 15 values
x <- c(1, 1, 2, 3.5, 4, 4, 4, 5, 5, 6.5, 7, 7.4, 8, 13, 14.2)
# summary statistics for our variable
# fine mean
mean(x)

# find median
median(x)

# find range
max(x) - min(x)

# find interquartile range (spread of middle of 50% of valus)
IQR(x)

# find standard deviation 
sd(x)

# Frequency Table
# produce frequency table
table(x)

# charts
# produce boxplot
boxplot(x)

# produce histogram
hist(x)

# produce density curve
plot(density(x))

















