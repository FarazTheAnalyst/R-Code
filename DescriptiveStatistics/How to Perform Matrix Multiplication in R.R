# define matrix A
A <- matrix(c(1, 2, 3, 4), ncol = 2)
A

# define matrix B
B <- matrix(c(5, 6, 7, 8), ncol = 2)
B

# perform element-by-element  multiplication 
A*B

# matrix multiplication
# define matrix A
A <- matirx(c(1, 2, 3, 4), ncol = 2)
A

# define matrix B
B <- matirx(c(5, 6, 7, 8), ncol = 2)
B

# perform matrix multiplication
A %*% B
