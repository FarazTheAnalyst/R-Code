# Example1: Calculate Square Using ^
# define variable
x <- 5

# calculate square of variable
x^2

# define vector
x <- c(2, 5, 6, 9)

# calculate square of each value in vector
x^2

# define data frame
x <- data.frame(A=c(2, 4, 5, 7, 8),
                B=c(3, 3, 5, 9, 12),
                C=c(7, 7, 8, 9, 15))

# view data frame
x

# calculate square of each value in data frame
x^2

